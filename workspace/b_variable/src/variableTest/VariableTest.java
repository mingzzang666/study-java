package variableTest;

public class VariableTest {
		public static void main(String[] args) {
//				정수형, 변수명은 a, 값은 10, 단, 기본형으로 선언한다.
				int age = 10;
//				나이를 저장하는 부분
				System.out.println(age);
//				나이를 출력하는 부분
				
//				실수형, 변수명은 b, 값은 10.1563, 단 float로 선언한다.
				float floatNumber = 10.1563F;
//				실수형(float)의 값 및 표기법을 저장하는 부분
				System.out.println(floatNumber);
//				실수형(float)의 값 및 표기법을 출력하는 부분
				
//				실수형, 변수명은 c, 값은 10.1563, 단, double로 선언한다.
				double doubleNumber = 10.1563;
//				실수형(double)의 값을 저장
				System.out.println(doubleNumber);
//				실수형(double)의 값을 출력
				
				char grade = 'A';
//				문자형으로 등급을 저장하는 부분
				System.out.println(grade);
//				문자형으로 저장한 등급을  출력하는 부분
				String data = "ABC";
//				문자열 데이터를 저장하는 부분
//				
				System.out.println(data);
//				문자열 데이터를 출력하는 부분
			
//				각 변수를 사용하여 출력 메소드로 출력해본다.
				System.out.println(age);
//				나이를 출력하는 부분
				System.out.println(floatNumber);
//				실수형(float) 숫자를 출력하는 부분
				System.out.println(doubleNumber);
//				실수형(double) 숫자를 출력하는 부분
				System.out.println(grade);
//				문자형으로 저장한 등급을 출력하는 부분
				System.out.println(data);
//				문자열로 저장한 데이터를 출력하는 부분
				
		}

}
