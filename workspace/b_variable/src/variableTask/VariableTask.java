package variableTask;

public class VariableTask {
		public static void main(String[] args) {
//			이름, 나이, 키, 취미
//			4개의 변수를 선언하여 각 값으로 초기화한 뒤
//			모두 출력한다.
			
			String name = "구민경";
//			이름을 저장하는 부분
			int age = 31;
//			나이를 저장하는 부분
			double height = 163.4;
//			키를 저장하는 부분
			String hobby = "게임";
//			취미를 저장하는 부분
			
			System.out.println(name);
//			저장한 이름 출력
			System.out.println(age);
//			저장한 나이 출력
			System.out.println(height);
//			저장한 키 출력
			System.out.println(hobby);
//			저장한 취미 출력
		}

}
